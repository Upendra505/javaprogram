
public class DimentionalStars
{
    public static void main(String[] args)
    {
        int n= 10;
        for(int i=1; i<=10; i++)
        {

            for(int j=n-1; j>=0; j--)
            {
                System.out.print(i);
            }
            System.out.println();
        }

    }
}
